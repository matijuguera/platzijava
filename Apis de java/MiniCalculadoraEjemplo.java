import java.lang.Math;

class MiniCalculadoraEjemplo {
	
	public MiniCalculadoraEjemplo(){
	}
	//Ingresa un numero y calcula el valor absoluto.
	public double calcularValorAbsoluto(double n){
		return Math.abs(n);
	}
	//Ingreso un numero y calcula la raiz cuadrada.
	public double calcularRaizCuadrada(double n){
		return Math.sqrt(n);
	}
}
